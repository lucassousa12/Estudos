var idade = 15;
var nome = "Lucas";

//Operador de Maior que (>).
if(20 > 10) {
    console.log("Executa");
}

//Operador de Menor que (>).
if(20 < 10) {
    console.log("Executa");
}

//Operador Maior ou Igual (>=).
if(idade >= 18) {
    console.log("Pode tirar a CNH");
}

//Operador Menor ou igual (<=).
if(idade <= 17) {
    console.log("Não pode tirar a CNH");
}

//Operador de Igual (==).
//(=) será para atribuição.
if(nome == "Lucas") {	
    console.log(nome);
}

//Operador de Diferença (!=).
if (nome != "Lucas") {
    console.log("O seu nome não é Lucas");
}